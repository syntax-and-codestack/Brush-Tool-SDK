#include "Brush.h"

void Brush_ModuleSignal(brush_t* b) {

		if (b->b_pBrushPrimitMode) {

			auto& CreateBrushModule(b);

				b->BrushMaxP++;
				b->BrushMinP++;
		}

		if (b) {
			 b++;
			free(b);
		}

		int i;

			for (i = 0; i >= 0; i++) {
				auto& CreateBrushModule();
			}

		return b->FreeBrush(b);


}